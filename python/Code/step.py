i = 1
j = 1
maxn = 11
while i <= 10: #проверка числа в промежутке
    while j <= 10: #проверка степени в промежутке
        a = str(i ** j) #возведение в степень
        if len(a) != 11:
            a = " " * (11 - len(a)) + a #расчет для условия
        print(a, end="\t") #вывод
        j += 1
    print("\n") #вывод чисел
    j = 1
    i += 1